def main():
    # Dimensiones del rectángulo
    x = 4
    y = 6
    # Calcular área del rectángulo
    rect_area = x * y
    print("Área del rectángulo:", rect_area)

    # Dimensiones del triángulo
    base = 5
    altura = 8
    # Calcular área del triángulo
    tri_area = 0.5 * base * altura
    print("Área del triángulo:", tri_area)

main()